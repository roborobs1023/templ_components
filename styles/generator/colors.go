//GenerateColorClasses generates a class for each color in the colorMap witha opacity variable set to 1. example:"--text-opacity:1; This uses the classType variable to overwrite the word text."
func (cg *CSSBuilder) GenerateColorClasses(classType string, prefixes ...string) {
	class := classType

	if len(prefixes) > 0 {
		class = prefixes[0] + "\\:" + classType
	}

	for name, colors := range colorMap {
		for level, value := range colors {
			fmt.Fprintf(cg.Output, ".%s-%s-%s {\n --%s-opacity:1;\n color:rgba(%s, var(--%s-opacity));\n}\n", class, name, level, classType, value, classType)
		}
	}
}

func (cg *CSSBuilder) GenerateColorVariables() {
	for name, colors := range colorMap {
		for level, value := range colors {
			fmt.Fprintf(cg.Output, "--clr-%s-%s:rgba(%s);\n", name, level, value)
		}
	}

	cg.Output.WriteString(".color-primary {\n color:var(--clr-primary);\n}\n")
	cg.Output.WriteString(".color-secondary {\n color:var(--clr-secondary);\n}\n")
	cg.Output.WriteString(".color-tertiary {\n color:var(--clr-tertiary);\n}\n")
	cg.Output.WriteString(".color-success {\n color:var(--clr-success);\n}\n")
	cg.Output.WriteString(".color-warning {\n color:var(--clr-warning);\n}\n")
	cg.Output.WriteString(".color-error {\n color:var(--clr-error);\n}\n\n")
}
